// CompositionLocal for content color on surfaces
val LocalContentColor = staticCompositionLocalOf { Color.Unspecified }

/**
 * MTT Theme composable that wraps MaterialTheme with proper color scheme,
 * typography, and shapes. Supports dynamic color on Android 12+ with fallback
 * to custom color scheme.
 *
 * @param darkTheme Whether to use dark theme
 * @param dynamicColor Whether to use dynamic color (Android 12+)
 * @param content The content to be themed
 */
@Composable
fun MttTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        // Dynamic color is available on Android 12+
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    // Determine content color based on surface color
    val contentColor = if (darkTheme) {
        md_theme_dark_onSurface
    } else {
        md_theme_light_onSurface
    }

    // Set status bar and navigation bar colors
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.background.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
        }
    }

    CompositionLocalProvider(
        LocalContentColor provides contentColor
    ) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = Typography,
            shapes = Shapes,
            content = content
        )
    }
}

/**
 * Extension property to get the appropriate content color from MaterialTheme
 * based on the current surface.
 */
@Composable
fun contentColor(): Color = LocalContentColor.current

/**
 * Preview composable for light theme
 */
@Composable
fun MttThemeLightPreview(content: @Composable () -> Unit) {
    MttTheme(darkTheme = false, dynamicColor = false) {
        content()
    }
}

/**
 * Preview composable for dark theme
 */
@Composable
fun MttThemeDarkPreview(content: @Composable () -> Unit) {
    MttTheme(darkTheme = true, dynamicColor = false) {
        content()
    }
}
